let str = "무야호@무야호@무야호";
const regex =
  /(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))/;

if (str.match(regex)) {
  console.log("이메일 양식");
} else {
  console.log("이메일 양식아님");
}

